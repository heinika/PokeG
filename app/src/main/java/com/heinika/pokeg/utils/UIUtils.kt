val Number.dp: Int
  get() = (this.toInt() * Resources.getSystem().displayMetrics.density + 0.5f).toInt()

val Int.toBoolean: Boolean
  get() = this == 1

//val Int.isCnId: Boolean
//  get() = this == 12

val Int.isEnId: Boolean
  get() = this == 9

val Int.isJaId: Boolean
  get() = this == 1

val Number.toTypeColor: Color
  get() = when (this) {
    Type.NORMAL.typeId -> normalColor
    Type.FIGHTING.typeId -> fightingColor
    Type.FLYING.typeId -> flyingColor
    Type.POISON.typeId -> poisonColor
    Type.GROUND.typeId -> groundColor
    Type.ROCK.typeId -> rockColor
    Type.BUG.typeId -> bugColor
    Type.GHOST.typeId -> ghostColor
    Type.STEEL.typeId -> steelColor
    Type.FIRE.typeId -> fireColor
    Type.WATER.typeId -> waterColor
    Type.GRASS.typeId -> grassColor
    Type.ELECTRIC.typeId -> electricColor
    Type.PSYCHIC.typeId -> psychicColor
    Type.ICE.typeId -> iceColor
    Type.DRAGON.typeId -> dragonColor
    Type.DARK.typeId -> darkColor
    Type.FAIRY.typeId -> fairyColor
    else -> grassColor
  }

val Number.toGenerationColor: Color
  get() = when (this.toInt()) {
    Generation.GenerationI.id -> GenerationIColor
    Generation.GenerationII.id -> GenerationIIColor
    Generation.GenerationIII.id -> GenerationIIIColor
    Generation.GenerationIV.id -> GenerationIVColor
    Generation.GenerationV.id -> GenerationVColor
    Generation.GenerationVI.id -> GenerationVIColor
    Generation.GenerationVII.id -> GenerationVIIColor
    Generation.GenerationVIII.id -> GenerationVIIIColor
    else -> GenerationIColor
  }

